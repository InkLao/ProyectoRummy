    private int obtenerValorRango(String str) {
        String[] partes = str.split("-");

        // Verificar que hay al menos dos partes
        if (partes.length > 1) {
            // Obtener la parte despu√©s del guion y convertirla a int
            int numero = Integer.parseInt(partes[1]);
            return numero;
        }
        return 0;
    }
